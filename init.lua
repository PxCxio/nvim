-- Vim Freeze

_G.split_opened = false
_G.frozen_line = 0
_G.frozen_line_top = 0
_G.frozen_line_bottom = 0
_G.is_frozen_active = false

function _G.toggle_split()
	if _G.split_opened then
		if #vim.api.nvim_list_wins() == 1 then
			return
		end
		vim.cmd("wincmd o")
		_G.split_opened = false
		_G.is_frozen_active = false
	else
		local target_line = vim.fn.line(".")
		_G.frozen_line = target_line
		_G.frozen_line_top = target_line - 2
		_G.frozen_line_bottom = target_line + 18
		_G.create_split(target_line)
		_G.split_opened = true
		_G.is_frozen_active = true
	end
end

function _G.create_split(target_line)
	vim.cmd("normal! mz")
	vim.cmd("split")
	vim.wo.scrollbind = true
	vim.cmd("normal! " .. target_line .. "G")
	local winheight = vim.fn.winheight(0)
	local resize_count = math.floor(winheight / 5)
	vim.cmd("normal! " .. resize_count .. "<C-w>-")
	vim.cmd("wincmd p")
	vim.cmd("normal! " .. _G.frozen_line .. "G")
end

function _G.auto_toggle_split()
	if not _G.is_frozen_active then
		return
	end

	local current_line = vim.fn.line(".")
	if _G.split_opened and current_line <= _G.frozen_line_top then
		vim.cmd("wincmd o")
		_G.split_opened = false
	elseif not _G.split_opened and current_line > _G.frozen_line_top then
		_G.create_split(_G.frozen_line)
		_G.split_opened = true
	end
end

vim.api.nvim_create_autocmd({ "CursorMoved", "CursorMovedI" }, {
	group = vim.api.nvim_create_augroup("AutoToggleSplit", { clear = true }),
	callback = _G.auto_toggle_split,
})

